func (c *config) link() (err error) {
	if err = c.compileSource(toExt(crt0c, ".o"), cc.NewStringSource(crt0c, cc.CRT0Source)); err != nil {
		return err
	}

	fn := "a.out"
	if c.o != "" {
		fn = c.o
	}

	f, err := os.Create(fn)
	if err != nil {
		return err
	}

	defer func() {
		if e := f.Close(); e != nil && err == nil {
			err = e
		}
	}()

	b := bufio.NewWriter(f)

	defer func() {
		if e := b.Flush(); e != nil && err == nil {
			err = e
		}
	}()

	header := fmt.Sprintf(mainHeader, strings.Join(c.osArgs, " "), "crt.")
	l, err := ccgo.NewLinker(b, header, c.goos, c.goarch)
	if err != nil {
		return err
	}

	defer func() {
		if e := l.Close(); e != nil && err == nil {
			err = e
		}
	}()

	for _, fn := range c.objects {
		var f *os.File
		if f, err = os.Open(fn); err != nil {
			return err
		}

		defer f.Close()

		if err = l.Link(fn, bufio.NewReader(f)); err != nil {
			return fmt.Errorf("%s: %v", fn, err)
		}
	}

	return nil
}

func (c *config) compile(in string) (err error) {
	out := filepath.Base(toExt(in, ".o"))
	if c.c && c.o != "" {
		if len(c.args) > 1 {
			return fmt.Errorf("-o cannot be used with -c and multiple input files")
		}

		out = c.o
	}
	src, err := cc.NewFileSource2(in, true)
	if err != nil {
		return err
	}

	return c.compileSource(out, src)
}

func (c *config) compileSource(out string, src cc.Source) (err error) {
	c.objects = append(c.objects, out)
	if !c.c {
		c.remove = append(c.remove, out)
	}
	defs := []string{`
#define _DEFAULT_SOURCE 1
#define _POSIX_C_SOURCE 200809
#define _POSIX_SOURCE 1
#define __FUNCTION__ __func__ // gcc compatibility
#define __ccgo__ 1
`}
	for _, v := range c.D {
		defs = append(defs, fmt.Sprintf("#define %s", v))
	}

	builtin, err := cc.Builtin()
	if err != nil {
		return err
	}

	tweaks := &cc.Tweaks{
		// TrackExpand:   func(s string) { fmt.Print(s) },
		// TrackIncludes: func(s string) { fmt.Printf("[#include %s]\n", s) },
		EnableAnonymousStructFields: true,
		InjectFinalNL:               true,
	}
	tu, err := cc.Translate(tweaks, c.incPaths, c.sysPaths, []cc.Source{
		cc.NewStringSource("<defines>", strings.Join(defs, "\n")),
		builtin,
		src,
	}...)
	if err != nil {
		return err
	}

	f, err := os.Create(out)
	if err != nil {
		return err
	}

	defer func() {
		if e := f.Close(); e != nil && err == nil {
			err = e
		}
	}()

	b := bufio.NewWriter(f)

	defer func() {
		if e := b.Flush(); e != nil && err == nil {
			err = e
		}
	}()

	return ccgo.Object(b, c.goos, c.goarch, tu)
}
