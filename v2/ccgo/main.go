type config struct {
	D      []string // -D
	Wl     []string // -Wl,
	c      bool     // -c
	l      []string // -l
	o      string   // -o
	shared bool     // -shared

	arg0      string
	args      []string
	goarch    string
	goos      string
	incPaths  []string
	linkOrder []string
	objects   []string
	osArgs    []string
	remove    []string
	sysPaths  []string
}

func newConfig(args []string) (*config, error) {
	if len(args) == 0 {
		return nil, fmt.Errorf("no arguments to parse")
	}

	r := &config{
		arg0:     args[0],
		goarch:   env("GOARCH", runtime.GOARCH),
		goos:     env("GOOS", runtime.GOOS),
		incPaths: []string{"@"},
		osArgs:   args,
	}
	args = args[1:]
	for len(args) != 0 {
		switch arg := args[0]; {
		case strings.HasPrefix(arg, "-D"):
			a := strings.SplitN(arg, "=", 2)
			if len(a) == 1 {
				a = append(a, "1")
			}
			r.D = append(r.D, fmt.Sprintf("%s %s", a[0][2:], a[1]))
		case arg == "-c":
			r.c = true
		case arg == "-o":
			if len(args) < 2 {
				return nil, fmt.Errorf("-o option requires an argument")
			}

			r.o = args[1]
			args = args[1:]
		case arg == "-shared":
			r.shared = true
		case strings.HasPrefix(arg, "-l"):
			s := arg[2:]
			r.l = append(r.l, s)
			r.linkOrder = append(r.linkOrder, s)
		case strings.HasPrefix(arg, "-Wl,"):
			r.Wl = strings.Split(arg[4:], ",")
		case !strings.HasPrefix(arg, "-"):
			r.args = append(r.args, arg)
			r.linkOrder = append(r.linkOrder, arg)
		case
			strings.HasPrefix(arg, "-O"),
			strings.HasPrefix(arg, "-W"),
			strings.HasPrefix(arg, "-f"):

			// ignored
		default:
			return nil, fmt.Errorf("%s: error: unrecognized command line option %q", r.arg0, arg)
		}
		args = args[1:]
	}
	return r, nil
}

func main1(args []string) (r int, err error) {
	returned := false

	defer func() {
		e := recover()
		if !returned && r == 0 {
			err = fmt.Errorf("PANIC: %v\n%s", e, debug.Stack())
		}
	}()

	c, err := newConfig(args)
	if err != nil {
		return 2, err
	}

	if len(c.args) == 0 {
		return 2, fmt.Errorf(`
%s: fatal error: no input files
compilation terminated`, c.arg0)
	}

	if c.sysPaths, err = cc.Paths(true); err != nil {
		return 1, err
	}

	sysPaths, err := cc.Paths(false)
	if err != nil {
		return 1, err
	}

	c.sysPaths = append(c.sysPaths, sysPaths...)
	for _, in := range c.args {
		switch ext := filepath.Ext(in); ext {
		case ".c":
			if err = c.compile(in); err != nil {
				return 1, err
			}
		case ".o":
			c.objects = append(c.objects, in)
		default:
			return 1, fmt.Errorf("%s: file not recognized", in)
		}
	}
	if c.c {
		returned = true
		return 0, nil
	}

	if c.shared {
		if err = c.linkShared(); err != nil {
			returned = true
			return 1, err
		}

		returned = true
		return 0, nil
	}

	defer func() {
		for _, v := range c.remove {
			os.Remove(v)
		}
	}()

	if err := c.link(); err != nil {
		return 1, err
	}

	returned = true
	return 0, nil
}

